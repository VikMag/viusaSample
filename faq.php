Los casos considerados como especiales  requieren de información adicional para poder ser atendidos, estos casos no  podrán ser atendidos por medio del servicio online y requerirán el servicio  completo de VIusa. <br />
Los casos considerados como especiales son: </p>
       <ul>
         <li>Si se cuenta con una invitación  para viajar a los Estados Unidos ya sea por parte de algún familiar, empresa u  organización.</li>
         <li>Si considera que su situación  cuenta con alguna particularidad como: violaciones a la ley de migración o  cualquier otro delito cometido en los EUA.</li>
         <li>Si se está solicitando una visa  que no sea de turista y/o visitante temporal por negocios o placer.</li>
         <li>Si el solicitante es dependiente  económico mayor de 23 años.</li>
         <li>Personas que se encuentren en  unión libre.</li>
