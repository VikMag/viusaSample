       <p><em><u>He viajado muchas veces a los EUA y  estoy realizando un trámite de renovación de Visa, es cierto que el  procedimiento es fácil y recibiré un trato diferente a como cuando solicite mi  Visa anterior simple y sencillamente por estar renovando Visa?</u></em><br />
         <br />
         Los tramites de renovación de Visa en  ocasiones y bajo ciertas condiciones pueden ser mas agiles para algunos  solicitantes sin embargo esto no significa que el solo hecho de renovar una  Visa o el hecho de que se haya viajado a los EUA anteriormente en varias  ocasiones simplificara el trámite de renovación. En VIusa contamos con el  servicio de asesoría para estos casos, lo invitamos a conocer este servicio en  nuestra página web dentro de la pestaña de &ldquo;servicios&rdquo; para iniciar con este  proceso.</p>
       <p>&nbsp;</p>
       <p><em><u>Mi tramite de solicitud de Visa  Americana fue rechazado, En dónde puedo obtener información sobre este asunto?  Puedo volver a solicitar Visa Americana si me  fue negada?</u></em><br />
         <br />
         Su trámite de solicitud de Visa únicamente  puede ser autorizada por el oficial consular al momento de su entrevista, en  caso de haber sido rechazado, usted puede realizar su solicitud nuevamente. En  VIusa lo asesoramos para que vaya lo mejor preparado posible a su nueva  entrevista son embargo el éxito de su tramite depende única y exclusivamente de  su situación personal, laboral y de la sinceridad que demuestre en su trámite  de solicitud de Visa.<br />
         <br />
  <em><u>Sera necesario que deje mi pasaporte  en la embajada en caso de que mi visa sea aprobada?</u></em><br />
         <br />
         En la actualidad el consulado o embajada  americana se queda con su pasaporte en caso de que su visa haya sido aprobada,  esto para pegarla dentro de su pasaporte, esto no es una regla, el oficial  consular le indicara si es necesario dejar su pasaporte después de su trámite.<br />
         <br />
  <em><u>La Visa que me otorgaron no es una  mica o una visa &ldquo;Láser&rdquo; como en el pasado? ¿Qué sucederá cuando expire mi  pasaporte y la visa impresa en él siga vigente?</u></em><br />
  <br />
         En la actualidad todas las visas se pegan en  el pasaporte. Tienen la misma función de una mica o visa &ldquo;Láser&rdquo; las cuales ya  no son emitidas por la Embajada de los Estados Unidos en México. Esto puedo cambiar  sin previo aviso.<br />
         Si su pasaporte expira antes que la visa,  usted debe viajar con dos pasaportes (el vigente y el anterior donde viene  pegada la visa válida) <br />
         <br />
  <em><u>Tengo una Visa &ldquo;Indefinida&rdquo; o  familiar, aun puedo hacer uso de esta visa para entrar a los EUA?</u></em> <br />
         <br />
         Las visas &ldquo;indefinidas&rdquo; y/o familiares ya no  son válidas en ningún caso.<br />
         <br />
  <em><u>Como puedo saber si mi Visa  Americana aun está vigente?</u></em><br />
  <br />
         Todas las visas que no son &ldquo;indefinidas&rdquo;  tienen una fecha de expiración la cual está indicada en la visa bajo  &quot;Expiry date&quot;. Después de esa fecha, la visa ya no podrá ser  utilizada y es necesario renovarla.<br />
  <em><u><br />
  Qué hago si mi visa fue robada o  extraviada?</u></em><br />
  <br />
         Es necesario realizar un procedimiento  específico que solicita la embajada americana, para esto VIusa ofrece asesoría  personalizada según su caso, visite nuestra pestaña de &ldquo;servicios&rdquo; para conocer  los detalles.<br />
  <em><u><br />
  Puedo renovar mi visa antes de que  venza o tengo que hacerlo después de esa fecha?</u></em><br />
         <br />
         Usted puede renovar su visa antes de la fecha  de expiración. Su visa anterior será cancelada o retenida por un oficial  consular como parte del proceso.<br />
         <br />
       </p>
